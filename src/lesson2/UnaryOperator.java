package lesson2;

public class UnaryOperator
{
	public static void main(String[] args)
	{
		int a = 5;

		System.out.println("a сейчас равно: " + a++);
		System.out.println("a сейчас равно: " + a);
		System.out.println("a сейчас равно: " + --a);
		System.out.println("a сейчас равно: " + a--);
		System.out.println("a сейчас равно: " + ++a);

	}
}
